# Array Generation Function's 
import numpy as np
print("\n")
print("\n")

# 1. Arange()
arr = np.arange(1, 11, 2)
print(arr)
print("\n")
print("\n")


# 2. zeros()


#One-D
arr = np.zeros(6)
print(arr)
print("\n")
print("\n")

#Multi-D
arr = np.zeros((6, 6))
print(arr)
print("\n")
print("\n")

# 3. ones() - > Same as Zero

arr = np.ones(6)
print(arr)
print("\n")
print("\n")

# 4. LinearSpace - linspace()
arr = np.linspace(0, 1, 30)
print(arr)

print("\n")

print("\n")
